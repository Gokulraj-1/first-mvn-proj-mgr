package com.fssa.mgr.day19.solved;

public  class UserDetails { // create a class
	// initialize the variables
	int id;
	String name;
	String password;
	String email;

	public int getId() { // create a method to get id
		return id;
	}

	public void setId(int id) { // create a method to Set id
		this.id = id;
	}

	public String getName() { // create a method to get name
		return name;
	}

	public void setName(String name) { // create a method to Set name
		this.name = name;
	}

	public String getPassword() { // create a method to get Password
		return password;
	}

	public void setPassword(String password) { // create a method to Set Password
		this.password = password;
	}

	public String getEmail() { // create a method to get Email
		return email;
	}

	public void setEmail(String email) { // create a method to Set Email
		this.email = email;
	}
}
